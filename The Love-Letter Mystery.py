def theLoveLetterMystery(s):
    # Write your code here
    n = len(s)
    op = 0
    for i in range(n // 2):
        leftchar = s[i]
        rightchar = s[n - i - 1]
        op = op + abs(ord(leftchar) - ord(rightchar))
        
    return op
        

